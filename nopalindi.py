phi = 3.14

def luas_lingkaran():
  jari = float(input("Masaukkan jari-jari lingkaran: "))
  return phi * jari ** 2

def luas_persegi():
  sisi = float(input("Masukkan sisi persegi: "))
  return sisi ** 2

def luas_segitiga():
  alas = float(input("Masukkan alas segitiga: "))
  tinggi = float(input("Masukkan tinggi segitiga: "))
  return alas * tinggi
